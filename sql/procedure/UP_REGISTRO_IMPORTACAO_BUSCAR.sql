
IF OBJECT_ID('UP_REGISTRO_BUSCAR', 'P') IS NOT NULL
	DROP PROCEDURE UP_REGISTRO_BUSCAR;
GO

CREATE PROCEDURE UP_REGISTRO_BUSCAR (
	@ID INT = NULL
	, @IMPORTACAO_ID INT = NULL
	, @ATIVO BIT = 1
	, @NUMERO_SINISTRO VARCHAR(25) = NULL
    , @CIA VARCHAR(50) = NULL
    , @CLIENTE VARCHAR(50) = NULL
    , @PROCESSO VARCHAR(50) = NULL
    , @BILHETE VARCHAR(25) = NULL
